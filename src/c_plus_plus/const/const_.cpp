#include <iostream>

using namespace std;

int main() {

    const int x = 3;
    int y = 4;
    int z = 5;

    /** ************************************************** 华丽的分割 ******************************************************* */
    // x=5;//错误，不能对常量重新赋值

    /** ************************************************** 华丽的分割 ******************************************************* */
    const int *p = &x;
    // 等价于
    // int const *p = &x;

    // *p=5;//错误
    p = &y;
    y = 5;
    cout << &x << ", " << &y << ", " << p << ", " << *p << endl;

    /** ************************************************** 华丽的分割 ******************************************************* */
    int *const p2 = &y;
    *p2 = z;
    // p2 = &z;//错误
    cout << &y << ", " << p2 << ", " << y << ", " << *p2 << endl;

    /** ************************************************** 华丽的分割 ******************************************************* */
    int const &q = y;
    // q=z;//错误

    return 0;
}